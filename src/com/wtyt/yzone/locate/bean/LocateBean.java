package com.wtyt.yzone.locate.bean;

public class LocateBean {
	  private String mobileNo;
	  
	  private String areaX;
	  
	  private String areaY;
	  
	  private String areaName;
	  
	  private String locTime;

	/**
	 * @return the mobileNo
	 */
	public String getMobileNo() {
		return mobileNo;
	}

	/**
	 * @param mobileNo the mobileNo to set
	 */
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	/**
	 * @return the areaX
	 */
	public String getAreaX() {
		return areaX;
	}

	/**
	 * @param areaX the areaX to set
	 */
	public void setAreaX(String areaX) {
		this.areaX = areaX;
	}

	/**
	 * @return the areaY
	 */
	public String getAreaY() {
		return areaY;
	}

	/**
	 * @param areaY the areaY to set
	 */
	public void setAreaY(String areaY) {
		this.areaY = areaY;
	}

	/**
	 * @return the areaName
	 */
	public String getAreaName() {
		return areaName;
	}

	/**
	 * @param areaName the areaName to set
	 */
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	/**
	 * @return the locTime
	 */
	public String getLocTime() {
		return locTime;
	}

	/**
	 * @param locTime the locTime to set
	 */
	public void setLocTime(String locTime) {
		this.locTime = locTime;
	}


	  
	  

}
